package myutils

func GetMaxInt(n1, n2 int) int {
	if n1 > n2 {
		return n1
	} else {
		return n2
	}
}

func GetMinInt(n1, n2 int) int {
	if n1 < n2 {
		return n1
	} else {
		return n2
	}
}

func GetMaxInt64(n1, n2 int64) int64 {
	if n1 > n2 {
		return n1
	} else {
		return n2
	}
}

func GetMinInt64(n1, n2 int64) int64 {
	if n1 < n2 {
		return n1
	} else {
		return n2
	}
}
